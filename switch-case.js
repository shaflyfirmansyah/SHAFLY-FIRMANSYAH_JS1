// Input pilihan menu
var pilihan = 2;

// Menggunakan switch case untuk menentukan menu
switch (pilihan) {
  case 1:
    console.log("Ayam Bakar");
    break;
  case 2:
    console.log("Ikan Bakar");
    break;
  case 3:
    console.log("Es Teh Manis");
    break;
  case 4:
    console.log("Es Jeruk");
    break;
  default:
    console.log("Menu Tidak Ada");
}